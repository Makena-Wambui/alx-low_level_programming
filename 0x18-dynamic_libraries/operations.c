#include "main.h"
/**
 * add - this function adds two integers together
 * @a: a
 * @b: b
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - this function subtracts an integer from another
 * @a: a
 * @b: b
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - this function returns the product of two integers
 * @a: a
 * @b: b
 * Return: a * b
 */
int  mul(int a, int b)
{
	return (a * b);
}
/**
 * div- this function divides two integers
 * @a: a
 * @b: b
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - this function calculates the modulo of two integers
 * @a: a
 * @b: b
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
